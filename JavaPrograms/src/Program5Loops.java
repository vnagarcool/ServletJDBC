
public class Program5Loops {

	public static void main(String[] args) {
		for (int i=1; i<=5; ++i)
			//for (int i=50; i>=1; i--)
		{
			System.out.println("Welcome to my world from for loop");
		}
		
		//------------------------------------------------------------------
		
		int j=1;
		while(j<=5)
		{
			System.out.println("Welcome to my world from while loop");
			j++;
		}
		//--------------------------------------------------------------------
		int k=1;
		do 
		{
			System.out.println("Welcome to my world from do while loop");
			k++;
		}while(k<=-5);
		
	}
	

}
